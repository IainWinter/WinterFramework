struct System_FuelTank : SystemBase
{
	void Init()
	{
		Entity laserTank = CreateEntity();
		laserTank.Add<LaserTank>();
		
		FuelTank& tank = laserTank.Add<FuelTank>();
		tank.cells = SimpleSandWorld(40, 40);
		tank.inlet = { {10, 0}, {30, 0} };
		tank.outlet = { { 18, 39 }, { 19, 39 }, { 20, 39 }, { 21, 39 } };
		tank.mask =	"00000000________________________00000000"
					"00000______________________________00000"
					"000__________________________________000"
					"00____________________________________00"
					"00____________________________________00"
					"0______________________________________0"
					"0______________________________________0"
					"0______________________________________0"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"________________________________________"
					"0______________________________________0"
					"0______________________________________0"
					"0______________________________________0"
					"000__________________________________000"
					"00000______________________________00000"
					"00000000________________________00000000"
					"0000000000000______________0000000000000"
					"0000000000000000________0000000000000000"
					"00000000000000000______00000000000000000"
					"000000000000000000____000000000000000000"
					"000000000000000000____000000000000000000";

		tank.onEat = [this]()
		{
			Player& player = First<Player>();
			player.AttackFuelAlt += player.AttackFuelAdditionAlt;
		};

		for (auto [tank] : Query<FuelTank>())
		for (int i = 0; i < tank.cells.width * tank.cells.height; i++)
		if (tank.mask[i] == '0')
			tank.cells.Write(i, { Color(1, 0, 0, 0), true });
	}

	void Update()
	{
		for (auto [tank] : Query<FuelTank>())
		{
			tank.fuelTankUpdateTimer -= Time::DeltaTime();
			if (tank.fuelTankUpdateTimer > 0) continue;

			tank.fuelTankUpdateTimer = tank.fuelTankUpdateRate;

			if (tank.feed.size() > 0)
			{
				auto [x, y] = tank.inlet.at(get_rand((int)tank.inlet.size()));
				tank.cells.Write(x, y, { tank.feed.at(0) });
				tank.feed.pop_front();
			}

			if (tank.openOutlet)
			{
				auto [x, y] = tank.outlet.at(get_rand((int)tank.outlet.size()));
				
				if (!tank.cells.Test(x, y))
				{
					tank.onEat();
				}

				tank.cells.Write(x, y, { Color::from32(0) });
			}

			tank.cells.Update();
			tank.cells.DrawToTexture();
		}
	}
};